
package org.bettergenteam.bettergen.layer;

public class Vector2D {
    
    private final int x;
    private final int z;
    
    public Vector2D(int x, int z) {
        this.x = x;
        this.z = z;
    }
    
    public int getX() {
        return x;
    }
    
    public int getZ() {
        return z;
    }

}
